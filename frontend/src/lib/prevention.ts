// 감염병 유형별 예방수칙 힌트(공중보건 기본 — 카테고리 수준). 질병명 키워드로 보수적 매칭.
// 목적: 트렌드 카드의 잘못된 일괄 "손 씻기·기침 예절"(뎅기열·말라리아 등엔 오류) 교체.
// 확신 없으면 null → 화면은 중립 안내로 폴백(공식 상세는 질병상세/포털로 유도). 진단 아님·참고용.
const RULES: { test: RegExp; hint: string }[] = [
  { test: /뎅기|말라리아|지카|일본뇌염|황열|치쿤구니야|웨스트나일/, hint: '모기에 물리지 않게 기피제·긴 옷 (모기 매개)' },
  { test: /SFTS|중증열성|쯔쯔가무시|쓰쓰가무시|라임|진드기|털진드기/, hint: '야외활동 시 긴 옷·기피제로 진드기 주의 (진드기 매개)' },
  { test: /신증후군출혈열|유행성출혈열|한타|렙토스피라|렙토/, hint: '설치류·오염된 풀밭 접촉 주의 (설치류·환경 매개)' },
  { test: /장출혈성|병원성대장균|노로|로타|콜레라|장티푸스|파라티푸스|세균성이질|A형간염|E형간염|식중독|장염|비브리오/, hint: '손 씻기·음식 익혀 먹기·물 끓여 마시기 (식품·수인성)' },
  { test: /B형간염|C형간염|에이즈|HIV|매독|임질/, hint: '혈액·체액 노출 주의 (B형간염 등은 예방접종)' },
  { test: /독감|인플루엔자|코로나|폐렴|결핵|백일해|홍역|수두|풍진|성홍열|수족구|볼거리|이하선염|디프테리아|마이코플라스마|호흡기|감기/, hint: '손 씻기·기침 예절·예방접종 (호흡기 전파)' },
  { test: /레지오넬라/, hint: '냉방기·대형 급수시설 위생 관리 (환경성)' },
  { test: /엠폭스|원숭이두창|수족구/, hint: '밀접 접촉·피부 병변 접촉 주의 (접촉 전파)' },
]

export function preventionHint(name: string): string | null {
  if (!name) return null
  for (const r of RULES) if (r.test.test(name)) return r.hint
  return null
}
