export function relationHits(text: string): { rel: Relation; intent: RelIntent; words: string[] }[] {
  return REL_LEX.map((e) => ({ rel: e.rel, intent: e.intent, words: e.words.filter((w) => text.includes(w)) })).filter((h) => h.words.length > 0)
}

// 강한 치유/약대체 주장(완치 룰 경로) — 음식카드 우회용. beneficial(좋다·효과)은 미포함.
export function isCureClaim(text: string): boolean {
  return REL_LEX.some((e) => e.intent === 'cure' && e.words.some((w) => text.includes(w)))
}
export function isBeneficialClaim(text: string): boolean {
  return REL_LEX.some((e) => e.intent === 'beneficial' && e.words.some((w) => text.includes(w)))
}
export function isHarmfulClaim(text: string): boolean {
  return REL_LEX.some((e) => e.intent === 'harm' && e.words.some((w) => text.includes(w)))
}
