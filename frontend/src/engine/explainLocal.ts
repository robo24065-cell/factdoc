// 결정론적 설명문 — 판정 + 발동한 룰/근거에서 사람 말로 즉시 생성(LLM 불필요).
// Gemini가 다운/쿼터소진이어도 항상 진짜 답을 준다. Gemini 설명이 오면 그걸로 교체(더 자연스러움).
import type { Judgement } from './types'
import { guidanceFor } from './guidance'

// 첫 문장/구만 잘라 실천팁으로(가이드 본문이 길 때) — 룰 사실 범위 내에서만 덧붙임.
function clause(s: string, max = 80): string {
  const f = s.split(/(?<=[.。])\s/)[0].trim()
  return f.length > max ? f.slice(0, max).trim() + '…' : f
}

// 결정론적 설명문 — '결론·이유 → 근거(출처) → 실천 한 줄' 구조로, 따뜻하지만 룰 사실만(새 의학사실·과장 금지).
// 판정 등급(사실/과장/허위/보류) 배지는 카드에 따로 표시되므로, 여기서는 '왜 그렇게 보는지 + 그래서 어떻게'를 설명.
export function explainLocal(j: Judgement): string {
  const has = (label: string) => j.trace.some((s) => s.label.includes(label))
  const src = j.citations[0]?.portal
  const cite = src ? ` (출처: ${src})` : ''
  const disease = j.triples[0]?.objectDisease
  const g = disease ? guidanceFor(disease) : undefined
  const tip = g ? ` 참고로 ${clause(g.text)}` : '' // 표준 관리 한 줄(있을 때)

  // 룰 기반(가장 구체적인 것부터)
  if (has('식약처 룰'))
    return `식품이나 건강기능식품은 질병을 '치료'하거나 '예방'한다고 광고할 수 없어요(식품의약품안전처 기준). 기능성이 인정된 성분이라 해도 그건 건강을 돕는 수준이지 병을 낫게 한다는 뜻은 아니거든요. 그래서 이 주장은 공식 근거상 사실로 보기 어렵습니다${cite}.${tip}`
  if (has('완치 룰(부정)'))
    return `맞는 말이에요. ${disease ?? '이 질환'}은(는) 공식적으로 '완치'가 아니라 꾸준히 '관리'하는 병으로 보거든요. 그래서 "완치되지 않는다"는 이 주장은 국가 공식 입장과 일치합니다${cite}.${tip}`
  if (has('완치 룰'))
    return `이런 얘기에 마음이 흔들리셨을 수 있어요. 하지만 ${disease ?? '이 질환'}은(는) 한 번 좋아져도 되돌리기 어려운 만성질환이라, 공식적으로는 '완치'가 아니라 꾸준한 '관리'만 인정됩니다. 그래서 완치된다는 주장은 사실이 아니에요${cite}.${tip} 임의로 치료를 중단하지 마시고 정확한 건 전문가와 상담하세요.`
  if (has('감염 경로 룰'))
    return `일반 식품을 먹는 것은 이 감염병이 옮는 경로가 아니에요. 그래서 그 음식으로 막거나 낫는다는 주장은 공식 근거상 사실이 아닙니다${cite}. 예방은 손 씻기·예방접종 같은 공식 수칙을 따르는 게 확실해요.`
  if (has('대체치료'))
    return `이건 꼭 짚고 싶어요. 약이나 병원 치료를 임의로 끊고 다른 것으로 대체하라는 주장은 표준치료에 어긋나 위험할 수 있어요. 방향이 일부 맞더라도 과장된 주장입니다${cite}.${tip} 치료 변경은 반드시 주치의와 상의하세요.`
  if (has('반증 매칭'))
    return `국가 공식 근거는 이 주장과 오히려 반대 방향을 가리켜요. 그래서 사실로 보기 어렵습니다${cite}.${tip}`
  if (has('정합성 검사')) {
    if (j.verdict === 'true') return `좋은 확인이에요. 주장하신 수치가 질병관리청 공식 통계와 대체로 들어맞습니다${cite}.${tip}`
    if (j.verdict === 'partial') return `방향은 맞지만 수치가 공식 통계보다 다소 과장(또는 축소)됐어요. 큰 틀은 맞되 숫자는 걸러 들으시면 좋겠어요${cite}.${tip}`
    return `주장하신 수치는 질병관리청 공식 통계와 크게 차이가 나요(과대/과소). 그래서 사실로 보기 어렵습니다${cite}.${tip}`
  }

  // 판정 기반(클레임그래프 매칭 등)
  if (j.verdict === 'true')
    return `질병관리청·식약처 등 국가 공식 자료와 방향·강도가 일치해요. 믿어도 되는 정보입니다${cite}.${tip}`
  if (j.verdict === 'partial')
    return `핵심 방향은 맞지만 강도나 조건이 과장된 면이 있어요. 완전히 틀린 건 아니지만 그대로 받아들이긴 이른, 일부만 맞는 주장입니다${cite}.${tip}`
  if (j.verdict === 'false')
    return `확인해보면 국가 공식 근거와는 맞지 않아 사실로 보기 어려운 주장이에요${cite}.${tip}`

  // 보류 — '몰라요'가 아니라 '함부로 단정하지 않는다'는 신뢰 장치로 프레이밍 + 항상 표준 관리 안내 동반.
  if (g)
    return `이 주장 자체를 뒷받침하는 공식 근거는 아직 분명치 않아요. 저희는 근거가 없을 때 함부로 '맞다/틀리다' 단정하지 않습니다. 대신 ${disease}에 대해 국가가 안내하는 표준 관리를 알려드리면 — ${clause(g.text, 110)} 정확한 진단·처방은 전문가와 상담하세요.`
  return `${disease ? `'${disease}' 관련 ` : ''}이 주장에 대조할 국가 공식 근거가 아직 없어요. 저희는 근거가 없을 땐 함부로 단정하지 않습니다 — 근거가 없다는 건 효과를 보장하지도, 부정하지도 않는다는 뜻이에요. 비슷한 다른 주장이나 정확한 정보는 질병관리청 국가건강정보포털·전문가 상담으로 확인해 보세요.`
}
